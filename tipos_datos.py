#Ejemplo de tipos de datos

#Entero

Edad = 30
print(type(Edad), Edad)

#Numero con punto flotante

altura = 1.75
print(type(altura), altura)

#Cadena de texto

nombre = "Ana"
print(type(nombre), nombre)

#Boleanos

es_esudiante = True
print(type(es_esudiante), es_esudiante)

#None, ausencia de valor

direccion = None
print(type(direccion), direccion)